// "Slice" devuelve una parte del array dentro de un nuevo array con el final no incluido =>
const gatitos = ["marron", "blanco", "negro", "gris"];
//                 0          1        2        3
const sliceGatitos = gatitos.slice(0, 3); // [ 'marron', 'blanco', 'negro' ]
console.log(sliceGatitos);
